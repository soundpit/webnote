            <p>Using Webnote is easy. Immediately following login, you are presented with the Webnote Workspace.</p>

            <h4>The Binder</h4>
               <img src="assets/images/binder.jpg" id= "binder_image" class=".img-thumbnail" alt="Binder Image" />
            <p>The first thing you’ll notice is the Binder, located on the left hand-side. The Binder is where you can control all your Pages. The Binder is movable, able to be dragged around the screen so you can organize your layout your way.</p>

            <p>The Binder toolbar has three icons.</p>
            <ul>
              <li>Clicking the Plus icon adds a new page. It will be displayed in the workspace and a new menu item in the Binder will be added.</li>
              <li>Clicking a cog icon will bring up a settings menu where you can change settings of the project.</li>
              <li>Clicking the down-arrow button will save the current session</li>
            </ul>


            <p>If a page has been added, then a page menu link will appear below the toolbar. </p>
            <img src="assets/images/page_visible.jpg" id= "link_image" class=".img-thumbnail" alt="Link Image" />
            <ul>
              <li>	Clicking on the arrow icon will hide the page from the workspace. Don’t worry, it is still there. Just click on the red x and it will appear again.
              </li>
              <li>Clicking on the name of the menu item will highlight its associated page and bring it to the front of other pages.</li>
              <li>Clicking on the cog icon will bring up a settings menu where you can change various settings.</li>
              <li>Clicking on the trash icon will delete the page. It will also delete the menu item in the Binder.</li>
            </ul>

            <h4>The Page</h4>

            <img src="assets/images/page.jpg" id="page_image" class=".img-thumbnail" alt="Page Image" />

            <ul>
              <li>Once a page has been created, it will appear on the workspace.</li>
              <li>The page is movable, and can be dragged around the workspace</li>
              <li>The page is resizable. Just resize it like you would any window.</li>
              <li>You can write as much text as you like inside the page. A scroll-bar will appear if the amount of text is more than the current viewable text area</li>
              <li>Clicking on the page will highlight its associated menu item in the Binder.</li>
            </ul>
            <p>We hope you love using Webnote and find it easy and fun to use. If you have any further questions then please don't hesitate to contact us.</p>
            
